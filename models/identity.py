"""=============================================================================
Identity: useful for not modifying data at all.
============================================================================="""

from   torch import nn

# ------------------------------------------------------------------------------

class Identity(nn.Module):

    def __name__(self):
        return 'Identity'

    def __str__(self):
        return '<Identity>'

# ------------------------------------------------------------------------------

    def __init__(self, cfg, **kwargs):
        super(Identity, self).__init__()
        assert cfg.EMBEDDING_DIM == cfg.N_GENES

# ------------------------------------------------------------------------------

    def encode(self, x):
        return x

# ------------------------------------------------------------------------------

    def decode(self, x):
        return x

# ------------------------------------------------------------------------------

    def forward(self, x):
        return x
